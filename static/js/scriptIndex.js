$(document).ready(function(){
	$('span.lightboxClose').click(function(){
		hideLightbox();
	})
	$("#loginBtn").click(function(){
		showLightbox('#login');
		$('span.lightboxClose').click(function(){
			hideLightbox();
		})
	});
	$("#signupBtn").click(function(){
		showLightbox('#signup');
	});
	$("#forgetBtn").click(function(){
		hideLightbox();
		showLightbox('#forget');
	});
})
